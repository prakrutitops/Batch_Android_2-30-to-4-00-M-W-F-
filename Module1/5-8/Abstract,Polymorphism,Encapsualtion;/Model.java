//Encapsulation:Wrapping up of data or binding of data
class Model
{
    String name,city;
    
    //getter-setter
    
    public void setName(String name)
    {
        this.name=name;
    }
    public String getName()
    {
        return name;
    }
    
     public void setCity(String city)
    {
        this.city=city;
    }
    public String getCity()
    {
        return city;
    }
}